struct addrspace *
as_create(void) {
	struct addrspace *as = kmalloc(sizeof(struct addrspace));
	if (as == NULL) return NULL;

	as->text_seg = NULL;
	as->data_seg = NULL;

	as->text_pt = NULL;
	as->data_pt = NULL;
	as->stack_pt = NULL;

	as->vn = NULL;

	return as;
}

static
void
as_zero_region(paddr_t paddr, unsigned npages)
{
	bzero((void *)PADDR_TO_KVADDR(paddr), npages * PAGE_SIZE);
}

